"""
sync_config.py — AI Radio Echo
Generates config.js for the local HTML dashboard.
  - local / prod-models: reads SQLite, exports 20 most recent episodes.
  - prod-db / production: writes Supabase credentials so the dashboard
    fetches live data from the cloud.
config.js must be in .gitignore.
"""

import json
import os
import sqlite3
from pathlib import Path


_SQLITE_ENVS   = {"local", "prod-models"}
_SUPABASE_ENVS = {"prod-db", "production"}
_CONFIG_PATH   = Path("config.js")


def sync_env_to_config(env: str) -> None:
    """
    Write config.js for the given environment.

    Args:
        env: One of local | prod-models | prod-db | production.
    """
    if env in _SQLITE_ENVS:
        _write_sqlite_config()
    elif env in _SUPABASE_ENVS:
        _write_supabase_config()
    else:
        print(f"[Sync] Unknown environment '{env}'. config.js not updated.")


# ------------------------------------------------------------------ #
#  Local / prod-models: read SQLite, export 20 most recent episodes  #
# ------------------------------------------------------------------ #

def _write_sqlite_config() -> None:
    db_path = Path("ai_radio_dev.db")
    if not db_path.exists():
        print(f"[Sync] SQLite DB not found at '{db_path}'. config.js not written.")
        return

    try:
        conn = sqlite3.connect(str(db_path))
        conn.row_factory = sqlite3.Row
        cursor = conn.execute(
            "SELECT * FROM memory_log ORDER BY created_at DESC LIMIT 20"
        )
        rows = [dict(row) for row in cursor.fetchall()]
        conn.close()
    except Exception as e:
        print(f"[Sync] Failed to read SQLite: {e}")
        return

    config_data = {
        "mode":     "local",
        "episodes": rows,
    }
    _write_config_file(config_data)
    print(f"[Sync] Wrote {len(rows)} episode(s) to config.js (SQLite mode).")


# ------------------------------------------------------------------ #
#  prod-db / production: write Supabase credentials                  #
# ------------------------------------------------------------------ #

def _write_supabase_config() -> None:
    supabase_url = os.environ.get("SUPABASE_URL", "")
    supabase_key = os.environ.get("SUPABASE_KEY", "")
    website_url  = os.environ.get("WEBSITE_URL",  "")

    config_data = {
        "mode":         "supabase",
        "supabase_url": supabase_url,
        "supabase_key": supabase_key,
        "website_url":  website_url,
    }
    _write_config_file(config_data)
    print("[Sync] Wrote Supabase credentials to config.js.")


# ------------------------------------------------------------------ #
#  Shared writer                                                      #
# ------------------------------------------------------------------ #

def _write_config_file(data: dict) -> None:
    """Serialise `data` as a JS constant and write to config.js."""
    js_content = (
        "// Auto-generated by AI Radio — Echo. Do NOT commit this file.\n"
        f"const CONFIG = {json.dumps(data, indent=2, default=str)};\n"
    )
    _CONFIG_PATH.write_text(js_content, encoding="utf-8")
