"""
sync_config.py — AI Radio Echo
Writes config.js for the local HTML dashboard.
  - SQLite envs (local, prod-models): read 20 most recent episodes, embed as JSON.
  - Supabase envs (prod-db, production): write Supabase credentials only.
config.js must be in .gitignore.
"""

import json
import os
import sqlite3
from datetime import datetime

_CONFIG_JS_PATH = "config.js"
_SQLITE_DB_PATH = "ai_radio_dev.db"
_SQLITE_ENVS    = {"local", "prod-models"}
_SUPABASE_ENVS  = {"prod-db", "production"}

_JS_HEADER = "// Auto-generated by sync_config.py — DO NOT COMMIT (in .gitignore)\n"


def _read_sqlite_episodes(limit: int = 20) -> list[dict]:
    """Read the most recent episodes from the local SQLite database."""
    if not os.path.exists(_SQLITE_DB_PATH):
        print(f"[Config] SQLite DB not found at {_SQLITE_DB_PATH}. Writing empty episode list.")
        return []
    conn = sqlite3.connect(_SQLITE_DB_PATH)
    conn.row_factory = sqlite3.Row
    try:
        cur = conn.execute(
            "SELECT * FROM memory_log ORDER BY created_at DESC LIMIT ?", (limit,)
        )
        return [dict(row) for row in cur.fetchall()]
    except sqlite3.OperationalError as exc:
        print(f"[Config] SQLite read error: {exc}")
        return []
    finally:
        conn.close()


def _write_config_js(content: str):
    with open(_CONFIG_JS_PATH, "w", encoding="utf-8") as fh:
        fh.write(_JS_HEADER)
        fh.write(content)
    print(f"[Config] Written: {_CONFIG_JS_PATH}")


def sync_env_to_config(env: str):
    """
    Write config.js appropriate for `env`.
    SQLite envs: embed 20 most recent episodes as static JSON.
    Supabase envs: write credentials so the dashboard fetches live data.
    """
    if env in _SQLITE_ENVS:
        episodes = _read_sqlite_episodes(limit=20)
        episodes_json = json.dumps(episodes, indent=2, default=str)
        js_content = (
            "const CONFIG = {\n"
            '  mode: "local",\n'
            f'  generatedAt: "{datetime.utcnow().isoformat()}Z",\n'
            f"  episodes: {episodes_json}\n"
            "};\n\n"
            "if (typeof module !== 'undefined') module.exports = CONFIG;\n"
        )
        _write_config_js(js_content)

    elif env in _SUPABASE_ENVS:
        supabase_url = os.environ.get("SUPABASE_URL", "")
        supabase_key = os.environ.get("SUPABASE_KEY", "")
        if not supabase_url or not supabase_key:
            print("[Config] Warning: SUPABASE_URL / SUPABASE_KEY not set. config.js will have empty credentials.")
        js_content = (
            "const CONFIG = {\n"
            '  mode: "supabase",\n'
            f'  generatedAt: "{datetime.utcnow().isoformat()}Z",\n'
            f'  supabaseUrl: "{supabase_url}",\n'
            f'  supabaseKey: "{supabase_key}"\n'
            "};\n\n"
            "if (typeof module !== 'undefined') module.exports = CONFIG;\n"
        )
        _write_config_js(js_content)

    else:
        print(f"[Config] Unknown env '{env}'. Skipping config.js write.")
